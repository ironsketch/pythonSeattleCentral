# Below is looping through 'i' 7 times, when you use the keyword range
# it will iterate upwards. Although as it stands it will only print 0 - 6
for i in range (7):
    print (i)

# To seperate each iteration
print ("Done")
print('')

# Below is looping through 'i' 7 times, when you use the keyword range
# it will iterate upwards. This time we add 1 immediatly to i everytime
# changing it to now show 1 - 7
for i in range (7):
    print (i + 1)

print ("Done")
print('')

# A repetition from before but with only 1 - 5
for i in range (5):
    print (i + 1)

print ("Done")
